def preprocess_text(text):
    """1. –û—á–∏—Å—Ç–∫–∞: –Ω–∏–∂–Ω–∏–π —Ä–µ–≥–∏—Å—Ç—Ä, —É–±–∏—Ä–∞–µ–º –ø—É–Ω–∫—Ç—É–∞—Ü–∏—é, –ª–∏—à–Ω–∏–µ –ø—Ä–æ–±–µ–ª—ã"""
    text = re.sub(r'[^\w\s]', ' ', text.lower())  # —Ç–æ–ª—å–∫–æ –±—É–∫–≤—ã+—Ü–∏—Ñ—Ä—ã+–ø—Ä–æ–±–µ–ª—ã
    text = re.sub(r'\s+', ' ', text).strip()      # –Ω–æ—Ä–º–∞–ª–∏–∑—É–µ–º –ø—Ä–æ–±–µ–ª—ã
    return text.split()

def jaccard_similarity(text1, text2):
    """2. JACCARD: |A ‚à© B| / |A ‚à™ B| ‚Äî –ø–µ—Ä–µ—Å–µ—á–µ–Ω–∏–µ/–æ–±—ä–µ–¥–∏–Ω–µ–Ω–∏–µ —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö —Å–ª–æ–≤"""
    words1 = set(preprocess_text(text1))
    words2 = set(preprocess_text(text2))
    intersection = words1.intersection(words2)
    union = words1.union(words2)
    return len(intersection) / len(union) if union else 0

def tfidf_similarity(texts):
    """3. TF-IDF + –∫–æ—Å–∏–Ω—É—Å: —É—á–∏—Ç—ã–≤–∞–µ—Ç —á–∞—Å—Ç–æ—Ç—É —Å–ª–æ–≤ –∏ –∏—Ö –≤–µ—Å –≤ –∫–æ—Ä–ø—É—Å–µ"""
    vectorizer = TfidfVectorizer(
        max_features=1000,          # —Ç–æ–ø-1000 —Å–ª–æ–≤
        stop_words=None,            # –±–µ–∑ —Å—Ç–æ–ø-—Å–ª–æ–≤ (–Ω—É–∂–Ω—ã –≤—Å–µ —Ç–µ—Ä–º–∏–Ω—ã)
        lowercase=True,             # –Ω–∏–∂–Ω–∏–π —Ä–µ–≥–∏—Å—Ç—Ä
        ngram_range=(1, 2)          # —É–Ω–∏–≥—Ä–∞–º–º—ã + –±–∏–≥—Ä–∞–º–º—ã ("CUDA —è–¥—Ä–∞")
    )
    tfidf_matrix = vectorizer.fit_transform(texts)
    similarity_matrix = cosine_similarity(tfidf_matrix)
    return similarity_matrix

